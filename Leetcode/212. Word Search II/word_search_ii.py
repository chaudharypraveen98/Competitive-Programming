from collections import Counter
class TrieNode(object):
    def __init__(self):
        self.children = {}
        self.word = None

class Solution(object):
    def __init__(self):
        self.root = TrieNode()
        
    def findWords(self, board, words):
        """
        :type board: List[List[str]]
        :type words: List[str]
        :rtype: List[str]
        """
        board_chars = Counter(ch for row in board for ch in row)
        res = set()

        def dfs_search_at_index(parent,i=0, j=0):
            if i<0 or j <0 or i >= len(board) or j >= len(board[0]) or board[i][j] not in parent.children:
                return False
            char = board[i][j]
            board[i][j] = "#"
            child = parent.children[char]
            if(child.word):
                res.add(child.word)
                child.word = None
            temp = any(dfs_search_at_index(child ,i+p, j+q) for (p,q) in [(1,0),(0,1),(-1,0),(0,-1)])
            board[i][j] = char

            if not child.children:
                parent.children.pop(char)
            return temp
        

        for word in words:
            word_chars = Counter(word)
            # if we have move character count than board
            if all(count <= board_chars[ch] for ch, count in word_chars.items()):
                # Prunning in case word is similar from start
                word_to_add = word[::-1] if word_chars[word[0]]> word_chars[word[-1]] else word
                curr = self.root
                # simple insert in trieNode 211. Design Add and Search Words Data Structure
                for ch in word_to_add:
                    if ch not in curr.children:
                        curr.children[ch] = TrieNode()
                    curr = curr.children[ch]
                # storing word directly at the node itself
                curr.word = word

        root = self.root
        for i in range(len(board)):
            for j in range(len(board[0])):
                dfs_search_at_index(root, i, j)
        return list(res)
    

